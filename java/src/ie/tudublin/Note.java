package ie.tudublin;


public class Note 
{
    private int duration;
	private char note;

    public int getDuration() {
        return this.duration;
    }

    public void setDuration(int duration) {
        this.duration = duration;
    }

    public char getNote() {
        return this.note;
    }

    public void setNote(char note) {
        this.note = note;
    }
}